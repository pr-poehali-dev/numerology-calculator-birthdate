export function getCitiesForNumber(num: number): CityData[] {
  const primary = CITIES.filter(c => c.number === num);
  if (primary.length >= 3) return primary.slice(0, 5);
  // Дополняем смежными числами
  const adjacent = [num - 1, num + 1].flatMap(n => CITIES.filter(c => c.number === n));
  return [...primary, ...adjacent].slice(0, 5);
}

export function getCompatibilityScore(cityNumber: number, lifePathNumber: number): number {
  const diff = Math.abs(cityNumber - lifePathNumber);
  if (diff === 0) return 100;
  if (diff === 1) return 85;
  if (diff === 2) return 70;
  if (diff === 3) return 60;
  return 50;
}

export function getCompatibilityLabel(score: number): string {
  if (score >= 100) return "Идеальное совпадение";
  if (score >= 85) return "Очень высокая совместимость";
  if (score >= 70) return "Высокая совместимость";
  if (score >= 60) return "Хорошая совместимость";
  return "Возможна адаптация";
}
