export interface NumerologyResult {
  birthDate: string;
  lifePathNumber: number;
  destinyNumber: number;
  soulNumber: number;
  personalityNumber: number;
  maturityNumber: number;
  dayNumber: number;
  monthNumber: number;
  yearNumber: number;
  personalYear: number;
  karmaNumber: number | null;
  masterNumbers: number[];
}

function reduceToSingle(n: number, keepMaster = true): number {
  if (keepMaster && (n === 11 || n === 22 || n === 33)) return n;
  if (n < 10) return n;
  const sum = n.toString().split('').reduce((a, d) => a + parseInt(d), 0);
  return reduceToSingle(sum, keepMaster);
}

export function calculate(day: number, month: number, year: number): NumerologyResult {
  const dayR = reduceToSingle(day);
  const monthR = reduceToSingle(month);
  const yearDigits = year.toString().split('').reduce((a, d) => a + parseInt(d), 0);
  const yearR = reduceToSingle(yearDigits);

  const lifePath = reduceToSingle(dayR + monthR + yearR);
  const destiny = lifePath;
  const soul = reduceToSingle(dayR + monthR);
  const personality = dayR;
  const maturity = reduceToSingle(lifePath + destiny);
  const currentYear = new Date().getFullYear();
  const currentYearDigits = currentYear.toString().split('').reduce((a, d) => a + parseInt(d), 0);
  const personalYear = reduceToSingle(dayR + monthR + reduceToSingle(currentYearDigits));

  const masterNumbers: number[] = [];
  [lifePath, soul, personality, maturity, personalYear].forEach(n => {
    if (n === 11 || n === 22 || n === 33) masterNumbers.push(n);
  });

  const karmaNumbers = [13, 14, 16, 19];
  const rawSum = day + month + year;
  const karma = karmaNumbers.includes(rawSum % 100) ? rawSum % 100 : null;

  return {
    birthDate: `${day.toString().padStart(2, '0')}.${month.toString().padStart(2, '0')}.${year}`,
    lifePathNumber: lifePath,
    destinyNumber: destiny,
    soulNumber: soul,
    personalityNumber: personality,
    maturityNumber: maturity,
    dayNumber: dayR,
    monthNumber: monthR,
    yearNumber: yearR,
    personalYear,
    karmaNumber: karma,
    masterNumbers: [...new Set(masterNumbers)]
  };
}

export function getArcane(n: number): ArcaneData {
  if (ARCANES[n]) return ARCANES[n];
  const reduced = reduceToSingle(n);
  return ARCANES[reduced] || ARCANES[1];
}

